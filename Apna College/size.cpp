#include "bits/stdc++.h" //preprocessor directive
using namespace std;

int main()
{
    int a;
    float b;
    double c;
    long d;

    short int si;
    long int li;
    long long int lli;

    signed int ssi;
    unsigned int ui;

    cout << "Size of int : " << sizeof(a) << endl;
    cout << "Size of float : " << sizeof(b) << endl;
    cout << "Size of double : " << sizeof(c) << endl;
    cout << "Size of long : " << sizeof(d) << endl;
    cout << "Size of short int : " << sizeof(si) << endl;
    cout << "Size of long int : " << sizeof(li) << endl;
    cout << "Size of signed int : " << sizeof(ssi) << endl;
    cout << "Size of unsigned int : " << sizeof(ui) << endl;
    cout << "Size of long long int : " << sizeof(lli) << endl;

    return 0;
}